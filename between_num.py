# Q Write a program to check whetherthe given number is in between 1 and 100?


number=int(input('Enter the any number you want to check'))


if number!=0 and number<=100:
	print(f'Given number {number} is in between 1 to 100 ')

else:
	print(f'The given number {number} is not in between 1 to 100')


